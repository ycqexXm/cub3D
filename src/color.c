#include "cub3d.h"

uint32_t	make_color(uint8_t *pixel)
{
	uint8_t	r;
	uint8_t	g;
	uint8_t	b;
	uint8_t	a;

	r = *(pixel + 0);
	g = *(pixel + 1);
	b = *(pixel + 2);
	a = *(pixel + 3);
	return (r << 24 | g << 16 | b << 8 | a);
}
